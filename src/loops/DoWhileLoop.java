package loops;

public class DoWhileLoop {
    public static void main(String[] args) {
        //do while: the code is first executed then the condition is checked
        int i = 0;//starting point
        do{
            System.out.println(i);
            i++;//incrementation
        }while (i>5);//condition
       /* i++;
        System.out.println(i);*/
    }
}
