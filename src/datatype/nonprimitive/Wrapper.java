package datatype.nonprimitive;

public class Wrapper {
    public static void main(String[] args) {

        //byte - Byte
        Byte b = 123;
        byte by = 32;
        String myStr = b.toString();

        //short - Short
        Short s = 3562;

        //int - Integer
        Integer i = 67275292;

        //long - Long
        Long  l = 652486520L;

        //float - Float
        Float f = 1.64397354F;

        //double - Double
        Double d = 1.2974235525235;

        //boolean - Boolean
        Boolean boo = true;

        //char - Character
        Character character = '&';
    }
}
